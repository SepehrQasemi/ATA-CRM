const dictionaries: Record<Locale, Dictionary> = {
  en: {},
  fr,
  fa,
};

export function normalizeLocale(value?: string | null): Locale {
  if (!value) return "en";
  const lc = value.toLowerCase();
  if (lc.startsWith("fr")) return "fr";
  if (lc.startsWith("fa") || lc.startsWith("ar")) return "fa";
  return "en";
}

export function isRtlLocale(locale: Locale): boolean {
  return locale === "fa";
}

function formatTemplate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? `{${key}}`));
}

export function t(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  if (locale === "en") return formatTemplate(key, vars);
  const translated = dictionaries[locale][key] ?? key;
  return formatTemplate(translated, vars);
}

export function roleLabel(role: string, locale: Locale): string {
  if (role === "admin") {
    if (locale === "fr") return "Administrateur";
    if (locale === "fa") return "مدیر";
    return "Admin";
  }
  if (role === "commercial") {
    if (locale === "fr") return "Commercial";
    if (locale === "fa") return "فروش";
    return "Commercial";
  }
  if (locale === "fr") return "Utilisateur";
  if (locale === "fa") return "کاربر";
  return "User";
}
